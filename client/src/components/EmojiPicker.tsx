export default function EmojiPicker({ onEmojiSelect, onClose }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<string>(
    Object.keys(EMOJI_CATEGORIES)[0]
  );
  const pickerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target as Node)) {
        onClose();
      }
    };

    document.addEventListener("mousedown", handleClickOutside);
    return () => {
      document.removeEventListener("mousedown", handleClickOutside);
    };
  }, [onClose]);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
  };

  return (
    <div className="emoji-picker" ref={pickerRef}>
      <div className="emoji-picker__header">
        <div className="emoji-picker__tabs">
          {Object.keys(EMOJI_CATEGORIES).map((category) => (
            <button
              key={category}
              className={`emoji-picker__tab ${
                selectedCategory === category ? "active" : ""
              }`}
              onClick={() => setSelectedCategory(category)}
              title={category}
            >
              {category === "Smileys & People" && "😀"}
              {category === "Animals & Nature" && "🐶"}
              {category === "Food & Drink" && "🍕"}
              {category === "Activities & Sports" && "⚽"}
              {category === "Travel & Places" && "✈️"}
              {category === "Objects" && "💡"}
              {category === "Symbols" && "❤️"}
              {category === "Flags" && "🏁"}
            </button>
          ))}
        </div>
      </div>
      <div className="emoji-picker__content">
        <div className="emoji-picker__grid">
          {EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES].map(
            (emoji, index) => (
              <button
                key={`${emoji}-${index}`}
                className="emoji-picker__emoji"
                onClick={() => handleEmojiClick(emoji)}
                title={emoji}
              >
                {emoji}
              </button>
            )
          )}
        </div>
      </div>
    </div>
  );
}


